        <h3>Annonces</h3>
        <p>A l’occasion de la journée mondiale des compétences des jeunes, le 15 juillet 2023, retour sur le déploiement dans quatre nouveaux pays du programme DCLIC de formation aux métiers du numérique. 
Dans le cadre du volet « Métiers du numérique » du projet de l’OIF « D-CLIC, formez-vous au numérique », 1300 jeunes (dont 51% de femmes) ont été formés en 2022 dans 10 pays et 42% des formations se sont tenues en zones périurbaines ou rurales. En 2023, avec la reconduction des formations à Djibouti, Madagascar et au Mali, auxquels s’ajoutent les quatre nouveaux pays de déploiement (Burkina Faso, Mauritanie, Tchad, Sénégal), 1200 jeunes supplémentaires sont touchés par le projet D-CLIC. 

Afin de suivre de près la mise en œuvre du programme D-CLIC et sensibiliser les acteurs pour une bonne appropriation du projet, l’OIF a effectué des visites de terrain dans ces quatre nouveaux pays. Elles ont été réalisées par la Direction de la Francophonie économique et numérique et les Représentations extérieures de l’OIF (REPAN en Afrique du Nord, REPAO en Afrique de l’Ouest et REPAC en Afrique centrale).</p> 

 
